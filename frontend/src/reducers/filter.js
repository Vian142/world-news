import { SET_FILTER } from '../types';
import { filter } from './state';

//////////////////////////////////////////////////////////////
const reducer = (state = filter, action) => {
    switch(action.type) {
    case SET_FILTER:
        return action.filter;
    default:
        return state;
    }
};

//////////////////////////////////////////////////////////////
export default reducer;

