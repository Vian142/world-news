import {SET_FILTER} from '../actions';

//////////////////////////////////////////////////////////////
const reducer = (state = 'all', action) => {
    switch(action.type) {
    case SET_FILTER: return action.filter;
    default:
        return state;
    }
};

//////////////////////////////////////////////////////////////
export default reducer;

