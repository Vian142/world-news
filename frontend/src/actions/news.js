import { v4 } from 'uuid';
import { ADD_NEWS, DELETE_NEWS, ADD_VIEWS } from '../types';

//////////////////////////////////////////////////////////////
const addNews = ({ title, date, category, thumbnail }) => ({
    type: ADD_NEWS,
    payload: {
        _id: v4(),
        title,
        date,
        category,
        thumbnail,
        views: 0
    }
});

//////////////////////////////////////////////////////////////
const deleteNews = _id => ({
    type: DELETE_NEWS,
    payload: _id
});

//////////////////////////////////////////////////////////////
const addViews = _id => ({
    type: ADD_VIEWS,
    payload: _id
});

//////////////////////////////////////////////////////////////
export {
    addNews,
    deleteNews,
    addViews
};

