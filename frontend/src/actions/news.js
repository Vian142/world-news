export const ADD_NEWS = 'ADD_NEWS';
export const EDIT_NEWS = 'EDIT_NEWS';
export const DELETE_NEWS = 'DELETE_NEWS';
export const ADD_VIEW = 'ADD_VIEW';

//////////////////////////////////////////////////////////////
import {v4} from 'uuid';

//////////////////////////////////////////////////////////////
const addNews = ({title, date, category, thumbnail}) => ({
    type: ADD_NEWS,
    _id: v4(),
    title,
    date,
    category,
    thumbnail,
    views: 0
});

//////////////////////////////////////////////////////////////
const deleteNews = _id => ({
    type: DELETE_NEWS,
    _id
});

//////////////////////////////////////////////////////////////
const addView = news => ({
    type: ADD_VIEW,
    ...news,
    views: news.views + 1
});

//////////////////////////////////////////////////////////////
export {
    addNews,
    deleteNews,
    addView
};

